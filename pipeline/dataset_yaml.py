"""Ultralytics data.yaml generator.

Produces the YAML config that Ultralytics YOLO consumes for a given
(client, tier, split) configuration.

The YAML points at:
  - An images directory (with a manifest-based image list filter)
  - The corresponding labels directory (tier-filtered if non-full)
  - The unified class list (names) in YOLO id order
  - The val/test path for evaluation

Ultralytics YOLO supports two ways to specify the image list:
  (a) Path to a directory — uses every image in it
  (b) Path to a .txt file listing image paths — uses only listed images

We use (b) so the same converted labels directory can serve multiple
subsample manifests without copying images. The image list file is
generated alongside the YAML.

YAML output structure (Ultralytics format):

    path: /abs/path/to/dataset/root
    train: train_images_list.txt
    val: val_images_list.txt
    names:
      0: worker
      1: excavator
      ...
"""
from __future__ import annotations

import argparse
from pathlib import Path
from typing import Optional

from data_prep.schema import load_schema, ClassSchema
from pipeline.class_subsets import get_tier_yolo_ids


def write_image_list(
    image_list_path: Path,
    image_dir: Path,
    stems: list[str],
    image_ext: str = ".jpg",
) -> int:
    """Write a text file listing absolute image paths, one per line.

    Returns the number of images actually found (warns if any are missing).
    """
    image_list_path.parent.mkdir(parents=True, exist_ok=True)
    image_dir = image_dir.resolve()

    found = []
    missing = []
    for stem in stems:
        candidate = image_dir / f"{stem}{image_ext}"
        if candidate.exists():
            found.append(str(candidate))
        else:
            # Try common alternative extensions
            for ext in (".jpeg", ".png", ".JPG", ".PNG"):
                alt = image_dir / f"{stem}{ext}"
                if alt.exists():
                    found.append(str(alt))
                    break
            else:
                missing.append(stem)

    if missing:
        print(
            f"Warning: {len(missing)} of {len(stems)} images not found in "
            f"{image_dir} (e.g., {missing[:3]}). They will be excluded from "
            f"the image list."
        )

    image_list_path.write_text("\n".join(found) + "\n")
    return len(found)


def build_data_yaml(
    yaml_path: str | Path,
    dataset_root: str | Path,
    train_image_list: str,
    val_image_list: str,
    schema: ClassSchema,
    tier: str = "tier_full",
) -> Path:
    """Write a YOLO-format data.yaml file.

    Args:
        yaml_path: Output YAML path
        dataset_root: The 'path:' field for Ultralytics — root of all relative paths
        train_image_list: Path to train image list file (relative to dataset_root)
        val_image_list: Path to val/test image list file (relative to dataset_root)
        schema: Loaded ClassSchema
        tier: Tier name. The full class list is always written (Ultralytics needs all
            id slots filled); for non-full tiers, inactive classes are marked as
            '_inactive_<name>' so the model architecture still has all output channels
            but inactive classes never receive gradient signal from filtered labels.

    Returns:
        Path to the written YAML file.
    """
    yaml_path = Path(yaml_path)
    yaml_path.parent.mkdir(parents=True, exist_ok=True)

    active_yolo_ids = get_tier_yolo_ids(tier, schema)
    full_names = schema.class_names

    # Build names dict — keep all 17 slots, mark inactive ones with prefix
    name_lines = []
    for yid, name in enumerate(full_names):
        if yid in active_yolo_ids:
            name_lines.append(f"  {yid}: {name}")
        else:
            name_lines.append(f"  {yid}: _inactive_{name}")

    yaml_text = (
        f"# Auto-generated by pipeline/dataset_yaml.py\n"
        f"# tier: {tier}\n"
        f"# active_yolo_ids: {sorted(active_yolo_ids)}\n"
        f"path: {Path(dataset_root).resolve()}\n"
        f"train: {train_image_list}\n"
        f"val: {val_image_list}\n"
        f"\n"
        f"names:\n" + "\n".join(name_lines) + "\n"
    )
    yaml_path.write_text(yaml_text)
    return yaml_path


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    parser.add_argument("--yaml-path", required=True)
    parser.add_argument("--dataset-root", required=True)
    parser.add_argument("--train-image-list", required=True)
    parser.add_argument("--val-image-list", required=True)
    parser.add_argument(
        "--tier",
        default="tier_full",
        choices=["tier_shared", "tier_shared_cross", "tier_full"],
    )
    parser.add_argument("--schema", default=None)
    args = parser.parse_args()

    schema = load_schema(args.schema) if args.schema else load_schema()
    out = build_data_yaml(
        yaml_path=args.yaml_path,
        dataset_root=args.dataset_root,
        train_image_list=args.train_image_list,
        val_image_list=args.val_image_list,
        schema=schema,
        tier=args.tier,
    )
    print(f"Wrote data.yaml: {out}")


if __name__ == "__main__":
    main()
